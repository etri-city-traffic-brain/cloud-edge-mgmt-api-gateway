class Dashboard extends React.Component {
  state = {
    open: true,
  };


  handleStateLayout = (props) => {
    
      LayoutCursor = props;
  };

  handleDrawerOpen = () => {
    this.setState({ open: true });
  };

  handleDrawerClose = () => {
    this.setState({ open: false });
  };

  render() {
    const { classes } = this.props;

    return (
      <div className={classes.root}>
        <CssBaseline />
        <AppBar
          position="absolute"
          className={classNames(classes.appBar, this.state.open && classes.appBarShift)}
        >
          <Toolbar disableGutters={!this.state.open} className={classes.toolbar}>
            <IconButton
              color="inherit"
              aria-label="Open drawer"
              onClick={this.handleDrawerOpen}
              className={classNames(
                classes.menuButton,
                this.state.open && classes.menuButtonHidden,
              )}
            >
              <MenuIcon />
            </IconButton>
            <Typography
              component="h1"
              variant="h6"
              color="inherit"
              noWrap
              className={classes.title}
            >
              Dashboard
            </Typography>
            <IconButton color="inherit">
              <Badge badgeContent={4} color="secondary">
                <NotificationsIcon />
              </Badge>
            </IconButton>
          </Toolbar>
        </AppBar>

        <Drawer
          variant="permanent"
          classes={{
            paper: classNames(classes.drawerPaper, !this.state.open && classes.drawerPaperClose),
          }}
          open={this.state.open}
        >
          <div className={classes.toolbarIcon}>
            <IconButton onClick={this.handleDrawerClose}>
              <ChevronLeftIcon />
            </IconButton>
          </div>



          <Divider />

          <List component="nav">
              <ListItemLink1 to="/Dashboard/inbox" primary="Inbox" icon={<InboxIcon />} />
              <ListItemLink1 to="/Dashboard/drafts" primary="Drafts" icon={<DraftsIcon />} />
            </List>
            <Divider />
          <List component="nav">
            <ListItemLink2 to="/Dashboard/trash" primary="Trash" />
            <ListItemLink2 to="/Dashboard/spam" primary="Spam" />
          </List>
          
          <Divider />
         

          
        </Drawer>

        <main className={classes.content}>
          <div className={classes.appBarSpacer} />          
              <TitlebarGridList />
        </main>
      </div>
    );
  }
}

Dashboard.propTypes = {
  classes: PropTypes.object.isRequired,
};

export default withStyles(styles)(Dashboard);
